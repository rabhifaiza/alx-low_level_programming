#include "3-calc.h"
/**
 * op_add - sum of a + b
 * @a: 1 par
 * @b: 2 par
 * Return: a+b;
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sum of a - b
 * @a: 1 par
 * @b: 2 par
 * Return: a-b;
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - sum of a * b
 * @a: 1 par
 * @b: 2 par
 * Return: a*b;
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - sum of a / b
 * @a: 1 par
 * @b: 2 par
 * Return: a/b;
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - sum of a * b
 * @a: 1 par
 * @b: 2 par
 * Return: a*b;
 */
int op_mod(int a, int b)
{
	return (a % b);
}
